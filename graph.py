def calculatevrms(data,bias):
    total = 0
    conversion_factor = (3.3 / (1 << 12))
    mean = bias * conversion_factor

    for sample in data:
        production = (sample * conversion_factor)
        total+= (production - mean) ** 2

    vrms = math.sqrt(total / len(data))
    vrms = vrms * 150

